            <p class="p" align="justify">
              It was for the first time in
history that the meet was co-hosted by two IITs, and IIT
Bhubaneswar became the first second-generation IIT to co-host the
meet. Eight Sports were organised by IIT Kharagpur and 4 by IIT
Bhubaneswar.
The 54th Inter IIT Sports Main Meet took place from December 15th
to 21st, 2019 at the IIT Bhubaneswar campus and from December
14th to 22nd, 2019 at the IIT Kharagpur campus.
The four events co-hosted by IIT Bhubaneswar were Badminton,
Squash, Table-tennis and Volleyball. The institute had undertaken
massive renovations of the playfields in anticipation of the meet.
About 800 students from 23 different IITs participated in the Meet at
IIT Bhubaneswar.Considering all the events, on and off the field, in
the host and co-host campuses of Kharagpur and Bhubaneswar
respectively, IIT Kharagpur came first with a total of 122 points,
followed by IIT Delhi with 92 points. The 3 rd position was bagged by
IIT Madras with 87.1 points.
